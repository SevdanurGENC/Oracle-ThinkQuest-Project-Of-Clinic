    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
    
         Cls_List_Of_Treatment pb=new Cls_List_Of_Treatment();
    
    pb.setId(Integer.valueOf(jTextField1.getText()));
    pb.setName_of_treatment(jTextField2.getText());
    pb.setCost(jTextField3.getText());
        
    BLL_List_Of_Treatment bll=new BLL_List_Of_Treatment();
       
    try 
        {
            bll.INSERT(pb);
            JOptionPane.showMessageDialog(rootPane,"Record has been added to the system... ");
        } 
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(rootPane,"Error : "+ex.getMessage());
        }
    
        
    }//GEN-LAST:event_jButton3ActionPerformed

