    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        jComboBox1.removeAllItems();
   
      BLL_List_Of_Treatment bll=new BLL_List_Of_Treatment();
  
          try 
          {
                List<Cls_List_Of_Treatment>make_liste=bll.FINDALL();
                for(Cls_List_Of_Treatment i: make_liste)
          {
                jComboBox1.addItem(String.valueOf(i.getId()));
          }
        } 
        catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(rootPane, "Error : "+ex.getMessage());
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
   
       String take_id_no = jComboBox1.getModel().getSelectedItem().toString();
   
    Cls_List_Of_Treatment pb= new Cls_List_Of_Treatment();
    BLL_List_Of_Treatment bll=new BLL_List_Of_Treatment();
    
        try 
        {
            pb=bll.SELECT(Integer.valueOf(take_id_no));
            
            jTextField1.setText(take_id_no);
            jTextField2.setText(pb.getName_of_treatment());
            jTextField3.setText(pb.getCost());
      
        } 
        catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(rootPane, "Error : "+ex.getMessage());
        }

    }//GEN-LAST:event_jButton2ActionPerformed

