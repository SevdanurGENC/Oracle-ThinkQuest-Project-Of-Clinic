    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
      
      jComboBox1.removeAllItems();
   
      BLL_Appointments bll=new BLL_Appointments();
  
      try 
      {
            List<Cls_Appointments>make_liste=bll.FINDALL();
            for(Cls_Appointments i: make_liste)
       {
            jComboBox1.addItem(String.valueOf(i.getRow_id()));
       }
        } 
        catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(rootPane, "Error : "+ex.getMessage());
        }  
        
    }//GEN-LAST:event_jButton5ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        
    String take_id_no = jComboBox1.getModel().getSelectedItem().toString();
   
    Cls_Appointments pb= new Cls_Appointments();
    BLL_Appointments bll=new BLL_Appointments();
    
    
        try 
        {
            pb=bll.SELECT(Integer.valueOf(take_id_no));
            
            jTextField1.setText(take_id_no);
            jTextField2.setText(pb.getPatient_name().getName());
            jTextField3.setText(pb.getPatient_surname().getSurname());
            jTextField4.setText(pb.getDoctor_name().getDr_name());
            jTextField5.setText(pb.getDoctor_surname().getDr_surname());
            jTextField6.setText(pb.getAppointment_time());
            jTextField7.setText(pb.getAppointment_date());
            jTextField8.setText(pb.getBranch());

        } 
        catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(rootPane, "Error : "+ex.getMessage());
        }

        
        
    }//GEN-LAST:event_jButton4ActionPerformed

