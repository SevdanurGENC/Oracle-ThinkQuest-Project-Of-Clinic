    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
    Cls_Appointments pb=new Cls_Appointments();
    
    pb.setRow_id(Integer.valueOf(jTextField1.getText()));
    
    Cls_Patients aa = new Cls_Patients();
    aa.setName(jTextField2.getText());
    aa.setSurname(jTextField3.getText());
    pb.setPatient_name(aa);
    pb.setPatient_surname(aa);
    
    Cls_Doctors bb = new Cls_Doctors();
    bb.setDr_name(jTextField4.getText());
    bb.setDr_surname(jTextField5.getText());
    pb.setDoctor_name(bb);
    pb.setDoctor_surname(bb);
   
    pb.setAppointment_time(jTextField6.getText());
    pb.setAppointment_date(jTextField7.getText());
    pb.setBranch(jTextField8.getText());
    
    BLL_Appointments bll=new BLL_Appointments();
       
    try 
        {
            bll.INSERT(pb);
            JOptionPane.showMessageDialog(rootPane,"Record has been added to the system... ");
        } 
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(rootPane,"Error : "+ex.getMessage());
        }
    
        
    }//GEN-LAST:event_jButton1ActionPerformed

