    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
  
    String take_id_no = jComboBox1.getModel().getSelectedItem().toString();
   
    Cls_Personnel pb= new Cls_Personnel();
    BLL_Personnel bll=new BLL_Personnel();
    
        try 
        {
            pb=bll.SELECT(Integer.valueOf(take_id_no));
            
            jTextField1.setText(take_id_no);
            jTextField2.setText(pb.getTc_identity());
            jTextField3.setText(pb.getRegister_identity());
            jTextField4.setText(pb.getAuthorization_id().getAuthorizations());
            jTextField5.setText(pb.getName());
            jTextField6.setText(pb.getSurname());
            jTextField7.setText(pb.getMisson());
            jTextField8.setText(pb.getDepartment());           
     
        } 
        catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(rootPane, "Error : "+ex.getMessage());
        }

    }//GEN-LAST:event_jButton2ActionPerformed

