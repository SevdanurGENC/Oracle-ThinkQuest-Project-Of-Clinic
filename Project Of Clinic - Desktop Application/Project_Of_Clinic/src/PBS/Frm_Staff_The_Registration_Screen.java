    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

    Cls_Personnel pb=new Cls_Personnel();
    
    pb.setId(Integer.valueOf(jTextField1.getText()));
    pb.setTc_identity(jTextField2.getText());
    pb.setRegister_identity(jTextField3.getText());

    Cls_Authorizations aut = new Cls_Authorizations();
   
    aut.setId(Integer.valueOf(jTextField6.getText()));
    pb.setAuthorization_id(aut);
    
    pb.setName(jTextField4.getText());
    pb.setSurname(jTextField5.getText());
    pb.setMisson(jTextField7.getText());
    pb.setDepartment(jTextField8.getText());
   
    BLL_Personnel bll=new BLL_Personnel();
           
    try 
        {
            bll.INSERT(pb);
            JOptionPane.showMessageDialog(rootPane,"Record has been added to the system... ");
        } 
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(rootPane,"Error : "+ex.getMessage());
        }
        
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
      jComboBox1.removeAllItems();
   
      BLL_Personnel bll=new BLL_Personnel();
  
      try {
            List<Cls_Personnel>make_liste=bll.FINDALL();
            for(Cls_Personnel i: make_liste)
           {
                jComboBox1.addItem(String.valueOf(i.getId()));
             }
        } 
        catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(rootPane,"Error : " + ex.getMessage());
        }  
    }//GEN-LAST:event_jButton1ActionPerformed

